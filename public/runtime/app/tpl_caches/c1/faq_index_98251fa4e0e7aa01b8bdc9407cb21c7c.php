			var $muxfb_form=$("#muxfb_form");
			var $muxfb_form_user_name=$("#muxfb_form").find("input[name='user_name']");
			var $muxfb_form_tel=$("#muxfb_form").find("input[name='tel']");
			var $muxfb_form_content=$("#muxfb_form").find("textarea[name='content']");
